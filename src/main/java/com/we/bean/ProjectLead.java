package com.we.bean;

public class ProjectLead {
	private String whoHasFinalApproval;

	private String strategist;

	private String accountPartner;

	private String client;

	public String getWhoHasFinalApproval() {
		return whoHasFinalApproval;
	}

	public void setWhoHasFinalApproval(String whoHasFinalApproval) {
		this.whoHasFinalApproval = whoHasFinalApproval;
	}

	public String getStrategist() {
		return strategist;
	}

	public void setStrategist(String strategist) {
		this.strategist = strategist;
	}

	public String getAccountPartner() {
		return accountPartner;
	}

	public void setAccountPartner(String accountPartner) {
		this.accountPartner = accountPartner;
	}

	public String getClient() {
		return client;
	}

	public void setClient(String client) {
		this.client = client;
	}

}
